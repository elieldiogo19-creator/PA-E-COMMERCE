function irParaDetalhes(idClicado) {
    const produtoEncontrado = produtos.find(p => p.id === idClicado);

    if (produtoEncontrado) {
        
        localStorage.setItem('produtoSelecionado', JSON.stringify(produtoEncontrado));
        
        window.location.href = "detalhes.html";
    } else {
        console.error("Produto não encontrado!");
    }
}


const observador = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
        if (entry.isIntersecting) {
            // Se o card apareceu na tela:
            entry.target.classList.add('show'); 
        } else {
            // Se o card SUMIU da tela (quando você sobe a página):
            entry.target.classList.remove('show'); 
        }
    });
}, {
    threshold: 0.1 // O sensor percebe quando 10% do card aparece
});

// Ativa o vigia em todos os itens que têm a classe 'revelar'
const elementos = document.querySelectorAll('.revelar');
elementos.forEach((el) => observador.observe(el));